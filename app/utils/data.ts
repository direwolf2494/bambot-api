function getDateValues(timestamp=null) {
    let today: Date;

    if (timestamp !== null) today = new Date(timestamp * 1000);
    else today = new Date();

    return {
        timestamp: Math.ceil(today.getTime() / 1000),
        localString: today.toLocaleDateString('en-US', dateOptions)
    }
}

